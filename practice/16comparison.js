// == equal value
// === equal value and data
// > greater than
// < less than
// >= greater than or equal to
// <= less than or equal to
// != not equal value
// !== not equal value and data type

const a = 5;
const b = "5";
const c = 10;

console.log(a == b);
console.log(a === b);
console.log(a > c);
console.log(a < c);
console.log(a >= b);
console.log(a <= c);
console.log(a != b);
console.log(a !== b);