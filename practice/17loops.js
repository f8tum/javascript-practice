// loops do something as long as a condition is true
// WHILE LOOP
// DO WHILE LOOP
// FOR LOOP
// FOR EACH LOOP (for easy iteration over data structures)

for (let i = 0; i < 5; i++) 
{
  console.log(i);
}

let i = 0;
while (i < 5) 
{
  console.log(i);
  i++;
}

let j = 0;
do 
{
  console.log(j);
  i++;
} while (j < 5);

